package kr.happyjob.study.accounting.model;

public class AccTitleModel {

	private int acc_no;
	private int dac_no;
	private String acc_nm;
	private String dac_nm;
	private String dac_cate;
	private String dac_con;
	private String dac_stat;
	private String cd;
	private String name;	
	
	
	public String getCd() {
		return cd;
	}
	public void setCd(String cd) {
		this.cd = cd;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAcc_no() {
		return acc_no;
	}
	public void setAcc_no(int acc_no) {
		this.acc_no = acc_no;
	}
	public int getDac_no() {
		return dac_no;
	}
	public void setDac_no(int dac_no) {
		this.dac_no = dac_no;
	}
	public String getAcc_nm() {
		return acc_nm;
	}
	public void setAcc_nm(String acc_nm) {
		this.acc_nm = acc_nm;
	}
	public String getDac_nm() {
		return dac_nm;
	}
	public void setDac_nm(String dac_nm) {
		this.dac_nm = dac_nm;
	}
	public String getDac_cate() {
		return dac_cate;
	}
	public void setDac_cate(String dac_cate) {
		this.dac_cate = dac_cate;
	}
	public String getDac_con() {
		return dac_con;
	}
	public void setDac_con(String dac_con) {
		this.dac_con = dac_con;
	}
	public String getDac_stat() {
		return dac_stat;
	}
	public void setDac_stat(String dac_stat) {
		this.dac_stat = dac_stat;
	}
	@Override
	public String toString() {
		return "AccTitleModel [acc_no=" + acc_no + ", dac_no=" + dac_no + ", acc_nm=" + acc_nm + ", dac_nm=" + dac_nm
				+ ", dac_cate=" + dac_cate + ", dac_con=" + dac_con + ", dac_stat=" + dac_stat + ", cd=" + cd
				+ ", name=" + name + "]";
	}
	
	
}
